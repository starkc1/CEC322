		switch (currentState) {
			case State1:
				if (stateChanged) {
					printf("Press the Down key to go to State 2.\n\r");
					stateChanged = false;
					BSP_LCD_GLASS_DisplayString(myStr1);
					//BSP_LCD_GLASS_DisplayString_Pnt_Cln(myStr1, myStr1Point, myString1Colon);
				}
				if (DOWN_KEY_IS_PRESSED) {
					currentState = State2;
					stateChanged = true;
					break;
				}
				break;
			case State2:
				if (stateChanged) {
					printf("Press the Up key to go to State 1.\n\r");
					stateChanged = false;
					BSP_LCD_GLASS_DisplayString(myStr2);
					//BSP_LCD_GLASS_DisplayString_Pnt_Cln(myStr2, myStr1Point, myString1Colon);
				}
				//BSP_LCD_GLASS_ScrollSentence(myStr2, 1, SCROLL_SPEED_MEDIUM);
				if (UP_KEY_IS_PRESSED) {
					currentState = State1;
					stateChanged = true;
					break;
				}
				break;
			default:
				printf("Something is deadly wrong if you see this state.\n\r");
				break;
		}
